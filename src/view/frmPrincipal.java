    private void btnSettingsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSettingsActionPerformed
        new ChangePanel(pnlContainer, new frmSettings());
        if (this.btnSettings.isSelected()) {
            this.pnlSettings.setBackground(new Color(51, 153, 103));//color verde
            this.btnSettings.setColorNormal(new Color(204, 204, 204));
            this.btnSettings.setColorHover(new Color(204, 204, 204));
            this.btnSettings.setColorPressed(new Color(204, 204, 204));

            this.pnlHome.setBackground(new Color(239, 238, 244));
            this.btnHome.setColorNormal(new Color(239, 238, 244));
            this.btnHome.setColorHover(new Color(204, 204, 204));
            this.btnHome.setColorPressed(new Color(204, 204, 204));

            this.pnlPet.setBackground(new Color(239, 238, 244));
            this.btnPet.setColorNormal(new Color(239, 238, 244));
            this.btnPet.setColorHover(new Color(204, 204, 204));
            this.btnPet.setColorPressed(new Color(204, 204, 204));

            this.pnlOwner.setBackground(new Color(239, 238, 244));
            this.btnOwner.setColorNormal(new Color(239, 238, 244));
            this.btnOwner.setColorHover(new Color(204, 204, 204));
            this.btnOwner.setColorPressed(new Color(204, 204, 204));
        } else {
            this.pnlSettings.setBackground(new Color(239, 238, 244));
            this.btnSettings.setColorNormal(new Color(239, 238, 244));
            this.btnSettings.setColorHover(new Color(204, 204, 204));
            this.btnSettings.setColorPressed(new Color(204, 204, 204));
        }
    }//GEN-LAST:event_btnSettingsActionPerformed

